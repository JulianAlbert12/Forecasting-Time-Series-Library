function MLEloginclick() {
    var x = document.getElementById("MLEbox");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }

  function CONTloginclick() {
    var x = document.getElementById("CONTbox");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }